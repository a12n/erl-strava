-module(strava_gear).

%% Types
-export_type([gear/0, t/0]).

%% Gear functions
-export([gear/2]).

%%%===================================================================
%%% Types
%%%===================================================================

-type gear() :: map().

-type t() :: gear().

%%%===================================================================
%%% Gear functions
%%%===================================================================

%%--------------------------------------------------------------------
%% @doc
%% Retrieve gear.
%% @end
%%--------------------------------------------------------------------
-spec gear(strava:auth_token(), binary()) -> t().

gear(Token, Id) ->
    case strava_api:read(Token, [<<"gear">>, Id], _Opts = #{}) of
        {ok, JSON} -> strava_json:to_gear(JSON)
    end.
